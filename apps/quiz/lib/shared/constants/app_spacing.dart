/// Constantes de espaciado y dimensiones para mantener consistencia en la UI.
///
/// Este archivo define todos los valores de espaciado, tamaños de iconos,
/// radios de borde y elevaciones usados en la aplicación.
/// NO usar valores numéricos directamente en los widgets.
abstract final class AppSpacing {
  AppSpacing._();

  // ==================== Espaciado vertical/horizontal ====================

  /// Espaciado muy pequeño: 4.0
  static const double tiny = 4.0;

  /// Espaciado pequeño: 8.0
  static const double small = 8.0;

  /// Espaciado mediano: 12.0
  static const double medium = 12.0;

  /// Espaciado grande: 16.0
  static const double large = 16.0;

  /// Espaciado muy grande: 18.0
  static const double xlarge = 18.0;

  /// Espaciado extra grande: 20.0
  static const double xlarge20 = 20.0;

  /// Espaciado doble grande: 24.0
  static const double xxlarge = 24.0;

  /// Espaciado triple grande: 32.0
  static const double xxxlarge = 32.0;

  /// Espaciado cuádruple grande: 48.0
  static const double xxxxlarge = 48.0;

  // ==================== Anchos máximos ====================

  /// Ancho máximo para contenido: 600.0
  static const double maxContentWidth = 600.0;

  /// Ancho máximo para formularios: 400.0
  static const double maxFormWidth = 400.0;

  // ==================== Radios de bordes ====================

  /// Radio pequeño: 4.0
  static const double smallRadius = 4.0;

  /// Radio mediano: 8.0
  static const double mediumRadius = 8.0;

  /// Radio grande: 12.0
  static const double largeRadius = 12.0;

  /// Radio circular: 999.0
  static const double circularRadius = 999.0;

  // ==================== Elevaciones ====================

  /// Elevación baja: 2.0
  static const double lowElevation = 2.0;

  /// Elevación media: 4.0
  static const double mediumElevation = 4.0;

  /// Elevación alta: 8.0
  static const double highElevation = 8.0;

  // ==================== Tamaños de iconos ====================

  /// Icono pequeño: 16.0
  static const double smallIcon = 16.0;

  /// Icono mediano: 24.0
  static const double mediumIcon = 24.0;

  /// Icono grande: 32.0
  static const double largeIcon = 32.0;

  /// Icono extra grande: 48.0
  static const double xlargeIcon = 48.0;
}
